package HomeworkWeek3;

public class ForLoopHomeworkOddEvenNoIfs {
    public static void main(String[] args) {
        for(int i=2;i<=20;i+= 2) {
            System.out.println("Even Number: " + i);
        }
        for(int b=1;b<=20;b+= 2) {
            System.out.println("Odd Number: " + b);
        }
    }
}
